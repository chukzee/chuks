    private void txtCrimeControlSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCrimeControlSaveActionPerformed

        Object status = this.cboCrimeControlStatus.getSelectedItem();

        if (status == null) {
            JOptionPane.showMessageDialog(this, "Status not selected!", "Invalid", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (status.toString().equals("ARRESTED")) {

            JComponent[] except_fields = {};//no exceptions
            if (!ServerManager.validateEmptyInput(this, except_fields)) {
                JOptionPane.showMessageDialog(this, "Field is empty", "Invalid", JOptionPane.ERROR_MESSAGE);
                return;
            }

        } else if (status.toString().equals("WANTED")) {
            if (this.txtCrimeControlFirtName.getText().isEmpty()
                || this.txtCrimeControlFirtName.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Full name of wanted person is required.", "Invalid", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        //Photos will be sent first
        String img_file_name = this.imgCrimeControlPhoto.getImageString();

        //get unique number for photo
        long unique_number = ServerManager.getUniqueNumber();
        if (unique_number == -1) {
            int option = JOptionPane.showConfirmDialog(this, "Initialize photo send failed!\nCheck your connection.\nTry again?", "Unsent Photo", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (option == JOptionPane.NO_OPTION) {
                return;
            }
            txtCrimeControlSaveActionPerformed(evt);
        }

        //send photo
        String remote_staff_photo_filename = ServerManager.sendPhotoToServerByFTP(img_file_name, unique_number);
        if (remote_staff_photo_filename == null || remote_staff_photo_filename.isEmpty()) {
            int option = JOptionPane.showConfirmDialog(this, "Could not send photo!\nCheck your connection.\nTry again?", "Unsent Photo", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (option == JOptionPane.NO_OPTION) {
                return;
            }
            txtCrimeControlSaveActionPerformed(evt);
        }

        SQLColumn[] sql_columns = {new SQLColumn("STATION_CATEGORY", ServerManager.division),
            new SQLColumn("STATE", ServerManager.IncidenceState),
            new SQLColumn("LGA", ServerManager.IncidenceLGA),
            new SQLColumn("FIRST_NAME", this.txtCrimeControlFirtName.getText()),
            new SQLColumn("SURNAME", this.txtCrimeControlSurname.getText()),
            new SQLColumn("SEX", this.txtCrimeControlSex.getSelectedItem()),
            new SQLColumn("DATE_OF_BIRTH", this.txtCrimeControlDateOfBirth.getEditor().getText()),
            new SQLColumn("PHONE_NO", this.txtCrimeControlPhoneNo.getText()),
            new SQLColumn("CASE_STATUS", status.toString()),
            new SQLColumn("REASON_FOR_ARREST", this.txtCrimeControlReasonOfArrest.getText()),
            new SQLColumn("DATE_OF_ARREST", this.txtCrimeControlDateArrested.getEditor().getText()),
            new SQLColumn("PHOTO_FILE_NAME", this.imgCrimeControlPhoto.getImageString())};

        DBResource dBResource = null;

        try {

            dBResource = ServerManager.insertStatementAutoPolice("crime_case", sql_columns, true);
            JOptionPane.showMessageDialog(this, "Record has been sucessfully added.",
                "Successful", JOptionPane.OK_OPTION);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Failed to add record.",
                "Failed", JOptionPane.OK_OPTION);
            ex.printStackTrace();
        } finally {
            if (dBResource != null) {
                dBResource.close();
            }
        }
    }//GEN-LAST:event_txtCrimeControlSaveActionPerformed

