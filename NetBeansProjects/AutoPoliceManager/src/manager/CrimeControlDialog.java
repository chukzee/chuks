    synchronized private void searchPreviouCaseOnPerson(String first_name, String surname) {

        DBResource dBResource = null;

        try {

            String columns[] = new String[]{"SN",
                "FIRST_NAME",
                "SURNAME",
                "SEX",
                "DATE_OF_BIRTH",
                "PHONE_NO",
                "REASON_FOR_ARREST",
                "DATE_OF_ARREST",
                "CASE_STATUS",
                "STATUS_DATE",
                "STATE",
                "LGA",
                "STATION_CATEGORY",
                "PHOTO_FILE_NAME"};

            String condition = " WHERE FIRST_NAME='" + first_name + "'"
                    + "AND SURNAME='" + surname + "'";

            dBResource = ServerManager.sendQueryAutoPolice("crime_case", columns, condition);
            this.lstPreviousCaseSearchList.setListData(new Vector());//first clear
            Vector v = new Vector();
            ServerManager.crime_person = new HashMap();
            int index = -1;//used for track the position on the list of names
            ServerManager.PreviousCaseOfPerson = 0;
            while (dBResource.rs.next()) {
                String fname = dBResource.rs.getString("FIRST_NAME");
                String lname = dBResource.rs.getString("SURNAME");
                String full_name = fname + " " + lname;
                v.add(full_name);
                Person person = new Person();
                person.serial_no = dBResource.rs.getInt("SN");;
                person.first_name = fname;
                person.last_name = lname;
                person.age = dBResource.rs.getString("DATE_OF_BIRTH");
                person.sex = dBResource.rs.getString("SEX");
                person.phone_no = dBResource.rs.getString("PHONE_NO");
                person.reason_for_arrest = dBResource.rs.getString("REASON_FOR_ARREST");
                person.date_of_arrest = dBResource.rs.getString("DATE_OF_ARREST");
                person.photo_file_name = dBResource.rs.getString("PHOTO_FILE_NAME");
                person.case_status = dBResource.rs.getString("CASE_STATUS");
                person.status_date = dBResource.rs.getString("STATUS_DATE");
                person.state = dBResource.rs.getString("STATE");
                person.lga = dBResource.rs.getString("LGA");
                person.station = dBResource.rs.getString("STATION_CATEGORY");
                index++;
                ServerManager.crime_person.put(index, person);

                ServerManager.imageLoader.loadImage(person.photo_file_name, this.imgCrimeControlPhoto);//load the image
            }

            ServerManager.PreviousCaseOfPerson = index+1;
            this.lstPreviousCaseSearchList.setListData(v);

            if (ServerManager.PreviousCaseOfPerson > 0) {
                cmdCrimeControlRelatedCase.setEnabled(true);
                String _case = ServerManager.PreviousCaseOfPerson > 1 ? " cases " : " case ";
                lblPreviousCaseIndicator.setText("Found " + ServerManager.PreviousCaseOfPerson + _case
                        + " relating to " + first_name + " " + surname);
            }

        } catch (SQLException ex) {
            Logger.getLogger(ServerManager.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            if(dBResource!=null){
                dBResource.close();
            }
        }

    }

