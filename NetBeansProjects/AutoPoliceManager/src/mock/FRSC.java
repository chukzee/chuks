    private void lstFRSCListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstFRSCListValueChanged

        String licence_no = this.lstFRSCList.getSelectedValue().toString();

        JSONObject json = sendGet(ServerManager.http_host, ServerManager.http_port,
            "dist/mock/MockDetails",
            "search=licence_detials",
            "licence_no=" + licence_no);

        if (json == null) {
            return;
        }

        this.lblFRSCFirstName.setText(json.getString("first_name"));
        this.lblFRSCLastName.setText(json.getString("last_name"));
        this.lblFRSCMiddleName.setText(json.getString("middle_name"));
        this.lblFRSCLicenseNo.setText(json.getString("license_no"));
        this.lblFRSCPlateNo.setText(json.getString("plate_no"));
        this.lblFRSCAddress.setText(json.getString("address"));
        this.lblFRSCLGAOfOrigin.setText(json.getString("lga_of_origin"));
        this.lblFRSCOccupation.setText(json.getString("occupation"));
        this.lblFRSCPhoneNumber.setText(json.getString("phone_no"));
        this.lblFRSCReligion.setText(json.getString("religion"));
        this.lblFRSCSex.setText(json.getString("sex"));
        this.lblFRSCStateOrigin.setText(json.getString("state_of_origin"));

        ServerManager.imageLoader.loadImage(json.getString("photo_url"), imgFRSCPhoto);
    }//GEN-LAST:event_lstFRSCListValueChanged

