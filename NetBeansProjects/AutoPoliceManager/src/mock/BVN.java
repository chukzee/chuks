    private void cmdBVNSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBVNSearchActionPerformed

        JSONObject json = sendGet(ServerManager.http_host, ServerManager.http_port,
                "dist/mock/MockDetails",//REMIND use of dist - change server to dist instead of app
                "search=bvn_list",
                "bank_name=" + this.txtBVNBankName.getText(),
                "first_name=" + this.txtBVNFirstName.getText(),
                "middle_name=" + this.txtBVNMiddleName.getText(),
                "last_name=" + this.txtBVNLastName.getText());

        if (json == null) {
            return;
        }

        if (!json.has("bvn_found_list")) {
            return;
        }

        JSONArray json_arr = json.getJSONArray("bvn_found_list");
        int len = json_arr.length();
        Object[] bvn_arr = new Object[len];
        for (int i = 0; i < len; i++) {
            bvn_arr[i] = json_arr.getString(i);
        }
        this.lstBVNList.setListData(bvn_arr);
    }//GEN-LAST:event_cmdBVNSearchActionPerformed

    private void lstBVNListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstBVNListValueChanged

        String bvn = this.lstBVNList.getSelectedValue().toString();

        JSONObject json = sendGet(ServerManager.http_host, ServerManager.http_port,
                "dist/mock/MockDetails",
                "search=bvn_detials",
                "bvn=" + bvn);

        if (json == null) {
            return;
        }

        this.lblBVNFirstName.setText(json.getString("first_name"));
        this.lblBVNLastName.setText(json.getString("last_name"));
        this.lblBVNMiddleName.setText(json.getString("middle_name"));
        this.lblBVNBankName.setText(json.getString("bank_name"));
        this.lblBVNAddress.setText(json.getString("address"));
        this.lblBVNAccountNumber.setText(json.getString("account_no"));
        this.lblBVNLGAOfOrigin.setText(json.getString("lga_of_origin"));
        this.lblBVNOccupation.setText(json.getString("occupation"));
        this.lblBVNPhoneNumber.setText(json.getString("phone_no"));
        this.lblBVNReligion.setText(json.getString("religion"));
        this.lblBVNSex.setText(json.getString("sex"));
        this.lblBVNStateOrigin.setText(json.getString("state_of_origin"));
        
        ServerManager.imageLoader.loadImage(json.getString("photo_url"), imgBVNPhoto);
    }//GEN-LAST:event_lstBVNListValueChanged

